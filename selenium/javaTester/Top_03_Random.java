package javaTester;

import java.util.Random;

public class Top_03_Random {

    public static void main(String[] args) {
        Random rand = new Random();
        System.out.println("trinhtt2"+rand.nextBoolean() + "@gmail.com");
        System.out.println("trinhtt2"+rand.nextDouble() + "@gmail.com");
        System.out.println("trinhtt2"+rand.nextFloat() + "@gmail.com");
        System.out.println("trinhtt2"+rand.nextInt() + "@gmail.com");
        System.out.println("trinhtt2"+rand.nextLong() + "@gmail.com");
        System.out.println("trinhtt2"+rand.nextBoolean() + "@gmail.com");
        System.out.println("trinhtt2"+rand.nextInt(9999) + "@gmail.com");

    }
}
